    private void Enter() {
        Html object = new Html();
        if (!urlValida(linkh.getText())) {
            JOptionPane.showMessageDialog(null, "Invalid link", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            object.link = this.linkh.getText();
            new Vista().setVisible(true);
            this.setVisible(false);
            this.dispose();
        }
    }

    private boolean urlValida(String url) {     //Funci√≥n para validar URL
        try {
            (new URL(url)).openStream().close();
            return true;
        } catch (Exception e) {
        }
        return false;
    }
